/**
  * @brief This function handles TIM5 global interrupt.
  */
void TIM5_IRQHandler(void)
{
  /* USER CODE BEGIN TIM5_IRQn 0 */
	if(LVGL_ON_OFF == 1)
	{
		lv_tick_inc(1);									//lvgl 的 1ms 心跳
		LVGL_Flag ++;									//LVGL的任务管理器执行周期计数
	}
	KEY_COUNT_IT++;									//按键扫描周期计数
	Key_Scan();
	BEEP_IT_Counter++;								//蜂鸣器周期计数
	Battery_COUNT_IT++;
	Disp_TL_cnt++;
	Disp_TL_limit_cnt++;
	TimeOutCnt++;
	Time_cnt++;										//系统计时器
	APEX_disp_cnt++;
	Changing_disp_cnt++;							//充电显示计数
	ChargDisp_cnt++;
	Aging_cnt++;
	if(BEEP_IT_Counter >= 10)
	{
		BEEP_IT_Counter = 0;
		ENDO_BEEP();									//声音处理
	}
  /* USER CODE END TIM5_IRQn 0 */
  HAL_TIM_IRQHandler(&htim5);
  /* USER CODE BEGIN TIM5_IRQn 1 */

  /* USER CODE END TIM5_IRQn 1 */
}

