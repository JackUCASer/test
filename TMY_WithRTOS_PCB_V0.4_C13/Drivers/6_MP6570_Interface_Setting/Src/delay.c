#include "delay.h"

/**********************************************************************************************************
*	函 数 名: delay_us(uint32_t nus)
*	功能说明: us延时函数
*	形    参: uint32_t nus为要延时的us数.
*	返 回 值: 无
*	编 辑 者: 1：王昌盛		
*	修订记录: 1:
*	编辑日期: 1: 20210811 				         
**********************************************************************************************************/
void delay_us(uint32_t nus)
{		
	for(uint8_t i = 0; i< 84; i++)
		__NOP;
}

/**********************************************************************************************************
*	函 数 名: void delay_ms(uint16_t nms)
*	功能说明: ms延时函数
*	形    参: uint16_t nms为要延时的ms数.
*	返 回 值: 无
*	编 辑 者: 1：王昌盛		
*	修订记录: 1:
*	编辑日期: 1: 20210811 				         
**********************************************************************************************************/
void delay_ms(uint16_t nms)
{
	uint32_t i;
	for(i = 0; i < nms; i++) 
	{
		delay_us(1000);
	}
}

			 

