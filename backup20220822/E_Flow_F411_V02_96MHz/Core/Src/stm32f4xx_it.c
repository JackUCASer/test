/**
  * @brief This function handles USART2 global interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
	BaseType_t xHigherPriorityTaskWoken;
	if(__HAL_UART_GET_FLAG(&huart2,UART_FLAG_IDLE) != RESET)//	idle标志被置位
	{ 
		__HAL_UART_CLEAR_IDLEFLAG(&huart2);					//	清除标志位
		HAL_UART_DMAStop(&huart2); 							//  停止DMA传输，防止   
		RX_LEN =  RX_BUFFER_SIZE - __HAL_DMA_GET_COUNTER(&hdma_usart2_rx); 	//	总计数减去未传输的数据个数，得到已经接收的数据个数
		RECV_END_FLAG = 1;									//	接受完成标志位置1
		
		xHigherPriorityTaskWoken = pdFALSE;
		if(((RX_BUF[0]<<8) + RX_BUF[1]) == 0xA55A){
			lcd_cmd_t.ucCmdSize = RX_BUF[2];
			memcpy(lcd_cmd_t.ucRevCmd, &RX_BUF[3], lcd_cmd_t.ucCmdSize);
			xQueueSendFromISR(qLcd_cmd_handle, &lcd_cmd_t, &xHigherPriorityTaskWoken);
		}
		memset(RX_BUF, 0, RX_LEN);
	 }
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */
	RX_LEN = 0;	
	HAL_UART_Receive_DMA(&huart2, RX_BUF, RX_BUFFER_SIZE);	//	重新打开DMA接收
  /* USER CODE END USART2_IRQn 1 */
}

